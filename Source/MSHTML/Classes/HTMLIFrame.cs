        {
			if( null != _hTMLControlElementEvents_SinkHelper)
			{
				_hTMLControlElementEvents_SinkHelper.Dispose();
				_hTMLControlElementEvents_SinkHelper = null;
			}

			_connectPoint = null;
		}
        
